    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        String email = this.text_email_login.getText();
        String password = String.valueOf(this.text_password_login.getPassword());
        //CHECK fields
        if (email.equals("") || password.equals("")) {
            showMessageDialog(null, "Compilare i campi richiesti");
            return;
        }
        //CHECK email
        if (GuiUtility.isEmailCorrect(email) == false) {
            showMessageDialog(null, "Formato Email errato");
            return;
        }
        //CALL login method
        logged = false;

        try {
            Pair<Integer, UserData> res = serviceStub.login(email, CryptMD5.crypt(password));

            if (res.getLast() != null) {
                loggedUser = res.getLast();
                logged = true;
                serviceStub.addObserver(res.getLast().getNickname(), clientService);
            } else {
                switch (res.getFirst()) {
                    case 0:
                        showMessageDialog(null, "Email errata!");
                        break;
                    case 1:
                        showMessageDialog(null, "Password errata!");
                        break;
                    case 2:
                        VerifyUser verify = new VerifyUser(this, true, serviceStub);
                        verify.setVisible(true);
                        break;
                    case 3:
                        showMessageDialog(null, "Utente amministratore!");
                        break;
                    case 4:
                        showMessageDialog(null, "Utente già loggato!");
                        break;
                }

            }
        } catch (RemoteException ex) {
            Logger.getLogger(ControlFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (logged) {
            btn_home.setEnabled(true);
            btn_stats.setEnabled(true);
            btn_profile.setEnabled(true);
            btn_logout.setEnabled(true);
            card.show(jPanel_main, "home");
            gameList = clientService.getGamesList();
            this.fillGameTable(gameList);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

